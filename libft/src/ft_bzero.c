#include "../includes/libft.h"

void	ft_bzero(void *s, size_t n)
{
	int		i;
	char	*temp_s;

	i = 0;
	temp_s = (char *)s;
	while ((size_t)i < n)
	{
		temp_s[i] = 0;
		i++;
	}
}
